var formLoadCountMobile = 0;

window.iframeLoadedMobile = function() {
    formLoadCountMobile++;
    
    if (formLoadCountMobile === 2) {
        // Κατεβάζουμε το iframe για να φανεί το μήνυμα
        var iframe = document.getElementById('google-form-iframe-mobile');
        if (iframe) {
            iframe.style.top = "0px";
        }

        // Περιμένουμε 6 δευτερόλεπτα
        setTimeout(function() {
            var form = document.getElementById('quiz-form-mobile');
            var trigger = document.getElementById('quiz-trigger-mobile');
            
            if(form && trigger) {
                form.style.display = "none";
                trigger.style.display = "block";
            }
            
            // Κρύβουμε πάλι το iframe
            if (iframe) {
                iframe.style.top = "-335px";
            }
            
            formLoadCountMobile = 0; 
        }, 8000); 
    }
};
