// --- 3. Άνοιγμα/Κλείσιμο Φόρμας στο Κινητό ---
window.toggleQuizMobile = function(event) {
    var trigger = document.getElementById('quiz-trigger-mobile');
    var form = document.getElementById('quiz-form-mobile');
    
    if (form && trigger) {
        form.style.display = "block";
        trigger.style.display = "none";
        
        setTimeout(function() {
            document.addEventListener('click', closeQuizMobile);
        }, 100);
    }
};

function closeQuizMobile(event) {
    var form = document.getElementById('quiz-form-mobile');
    var trigger = document.getElementById('quiz-trigger-mobile');
    
    if (form && trigger && !event.target.closest('#quiz-form-mobile')) {
        form.style.display = "none";
        trigger.style.display = "block";
        document.removeEventListener('click', closeQuizMobile);
    }
}

