// 2. Η ΑΝΑΝΕΩΜΕΝΗ ΣΥΝΑΡΤΗΣΗ ΕΠΙΛΟΓΗΣ (Με τις νέες λέξεις-κλειδιά)
function generateMissionPC(factText, missionsObj) {
  const text = factText.toLowerCase();
  let category = 'default';
  
  if (text.includes("σελήν") || text.includes("διάστημ") || text.includes("πλανήτ") || text.includes("apollo") || text.includes("τηλεσκόπιο") || text.includes("δορυφόρ") || text.includes("άρη") || text.includes("αστρονόμ") || text.includes("ήλιο")) {
    category = 'space';
  } else if (text.includes("εφευρίσκει") || text.includes("υπολογιστ") || text.includes("apple") || text.includes("ρεύμα") || text.includes("λάμπα") || text.includes("αυτοκίνητ") || text.includes("windows") || text.includes("εφεύρεσ") || text.includes("μηχαν") || text.includes("τηλέφων")) {
    category = 'tech';
  } else if (text.includes("βιβλ") || text.includes("ταινία") || text.includes("ζωγράφ") || text.includes("μίκυ") || text.includes("χάρι") || text.includes("πικάσο") || text.includes("σκίτσο") || text.includes("συγγραφ") || text.includes("μουσικ") || text.includes("σινεμά")) {
    category = 'arts';
  } else if (text.includes("εξερευνητ") || text.includes("κολόμβος") || text.includes("μαγγελάνος") || text.includes("ωκεαν") || text.includes("νησ") || text.includes("καράβ") || text.includes("ήπειρ") || text.includes("διώρυγα") || text.includes("ταξίδ")) {
    category = 'exploration';
  } else if (text.includes("επανάστασ") || text.includes("απελευθερώνεται") || text.includes("μάχ") || text.includes("βασιλ") || text.includes("ιστορ") || text.includes("πόλεμ") || text.includes("αυτοκρατορ") || text.includes("σύνταγμα")) {
    category = 'history';
  } else if (text.includes("φυσικός") || text.includes("χημικ") || text.includes("νεύτων") || text.includes("αϊνστάιν") || text.includes("βαρύτητα") || text.includes("θεωρί") || text.includes("μάθημ") || text.includes("στοιχείο")) {
    category = 'science';
  } else if (text.includes("γιατρ") || text.includes("εμβόλι") || text.includes("πενικιλίνη") || text.includes("φάρμακ") || text.includes("ιός") || text.includes("δεινόσαυρ") || text.includes("φυτ") || text.includes("πάστερ")) {
    category = 'biology';
  } else if (text.includes("αγών") || text.includes("ολυμπιακ") || text.includes("ποδόσφαιρ") || text.includes("μπάσκετ") || text.includes("αθλητ") || text.includes("γυμναστικ")) {
    category = 'sports';
  }
  
  const missionsList = missionsObj[category];
  const randomIndex = Math.floor(Math.random() * missionsList.length);
  return missionsList[randomIndex];
}

// 3. Η συνάρτηση που ανοιγοκλείνει το πλαίσιο στο κλικ (Μετονομάστηκε για το PC)
function toggleMissionPC(event) {
  // Σταματάμε το κλικ από το να «ανέβει» στο document, ώστε να μην κλείνει αμέσως
  if (event) {
    event.stopPropagation();
  }
  
  const missionContainer = document.getElementById("mission-container-pc");
  if(missionContainer) {
    missionContainer.classList.toggle("open");
  }
}

// Συμπλήρωμα: Κλείσιμο όταν κάνουμε κλικ οπουδήποτε έξω
document.addEventListener("click", function(event) {
  const container = document.getElementById("history-wrapper-container-pc");
  const missionContainer = document.getElementById("mission-container-pc");
  
  // Αν το κλικ έγινε έξω από το κουτί και το συρτάρι είναι ανοιχτό, κλείσ' το
  if (container && !container.contains(event.target)) {
    if (missionContainer && missionContainer.classList.contains("open")) {
      missionContainer.classList.remove("open");
    }
  }
});

